package Collections;

import java.util.LinkedList;

public class ListExample {
    public static void main(String[] args) {
        LinkedList<Integer>l=new LinkedList<>();
        l.add(01);
        l.add(9);
        l.add(03);
        l.add(07);
        for(int i: l){
            System.out.println(i);
        }

    }
}
